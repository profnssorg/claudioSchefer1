# Declaração de variáveis




# Entrada de dados

temp_celsius = float(input("Digite temperatura em Celsius: "))


# Processamento

temp_farenheit = ((9*temp_celsius)/5)+32


# Saída de dados

print(" %5.2f ºC corresponde a %5.2f ºF" %(temp_celsius, temp_farenheit))

